int		ft_atoi(char *str)
{
	int i;
	int num;

	num = 0;
	i = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			i *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		num *= 10;
		num += *(str++) - '0';
	}
	return (i * num);
}
