#include <stdlib.h>

char	*ft_strdup(char *str)
{
	int		i;
	char	*p;

	i = 0;
	while (str[i])
		i++;
	p = (char *)malloc(sizeof(char) * i + 1);
	if (!p)
		return (0);
	i = 0;
	while (str[i])
	{
		p[i] = str[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
