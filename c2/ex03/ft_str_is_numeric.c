int		ft_str_is_numeric(char *str)
{
	int i;
	int n;

	i = 0;
	n = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 48 && str[i] <= 57)
			n++;
		i++;
	}
	if (n == i)
		return (1);
	else
		return (0);
}
